!      nh=n_in
!      nstp=1
    nl   = iStart
    nh   = iStop
    nstp = 1

    do ip=nl,nh,nstp
        fdif = abs(v(ip) - v0)
        j = min((nptc-2),int(fdif/dvc)) + 1
        fchi = vbc + (j - 1)*dvc
        df=(fdif-fchi)/dvc
    !   Do the air (actually N2) broadened part
        chil = chi(j,it)   + (chi(j+1,it)   - chi(j,it)  )*df
        chiu = chi(j,it+1) + (chi(j+1,it+1) - chi(j,it+1))*df
        chif = chil + &
        (chiu-chil)*(T-tchi(it))/(tchi(it+1)-tchi(it))
    !   Now do the pure cO2 part
