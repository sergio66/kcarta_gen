! this is the CO2 continuum, for contum.f in GENLN2
    SUBROUTINE co2_contnm(iL,raTAmt,raTTemp,raTPress,raTPartPress, &
    raFreq,iNumPts,daDumbAbs)

    IMPLICIT NONE
     
    include '../INCLUDE/TempF90/kcartaparam.f90'

! input params
    INTEGER :: iL,iNumPts
    REAL :: raTTemp(kProfLayer),raTAmt(kProfLayer),raTPress(kProfLayer)
    REAL :: raTPartPress(kProfLayer),raFreq(*)
! output param
    DOUBLE PRECISION :: daDumbAbs(*)

! local vars
    INTEGER :: iFr,mk,npt
    REAL :: ff,f1,gr,CFFF96,CFFF60,CFFF30,DT30,DT60,DT96,cffft,a4,ts,ps
    REAL :: vb,vt,dv

    REAL :: VB2,VT2,DV2,NPT2,CO2F96(2001),CO2F30(2001),CO2F60(2001)

    COMMON /CCO2F0/ VB2,VT2,DV2,NPT2,CO2F96
    COMMON /CCO2F2/ CO2F30
    COMMON /CCO2F1/ CO2F60

    ts = 296.0
    ps = 1.0

    VB = VB2
    VT = VT2
    DV = DV2
    NPT = NPT2
    IF (NPT == 0) THEN
        NPT = 2001
    END IF

!      print *,vb,vt,dv,npt
!      print *,iL, raTTemp(iL),raTPress(IL), raTamt(iL)
!      print *,ts,ps

    DO iFr = 1,iNumPts
        ff = raFreq(iFr)
        IF (FF >= VB .AND. FF < VT) THEN
            MK = 1 + INT((FF - VB)/DV)
            F1 = VB + (MK - 1)*DV
        ENDIF
    !         print *,iFr,ff,mk,f1
    
    !  LINEARLY INTERPOLATE IN FREQUENCY AT EACH TEMPERATURE
    
        GR = (FF - F1)/DV
        CFFF96 = CO2F96(MK) + GR*(CO2F96(MK+1)-CO2F96(MK))
        CFFF60 = CO2F60(MK) + GR*(CO2F60(MK+1)-CO2F60(MK))
        CFFF30 = CO2F30(MK) + GR*(CO2F30(MK+1)-CO2F30(MK))
    
    !  LAGRANGIAN INTERPOLATION IN TEMPERATURE
    
        DT30 = raTTemp(iL) - 230.
        DT60 = raTTemp(iL) - 260.
        DT96 = raTTemp(iL) - 296.
        CFFFT = 5.050505E-4*DT60*DT96*CFFF30 &
        - 9.259259E-4*DT30*DT96*CFFF60 &
        + 4.208754E-4*DT30*DT60*CFFF96
    
        A4 = raTamt(iL)*(raTPress(IL)/PS)*CFFFT
    
        daDumbAbs(iFr) = dble(a4)*kavog
        daDumbAbs(iFr) = dble(a4)
    END DO

    return
    end SUBROUTINE co2_contnm
