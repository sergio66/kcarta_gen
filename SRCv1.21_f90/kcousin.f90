    if (T >= tchi(2)) it = 2
    if (T < tchi(2)) it = 1

! this is for the self broadened part
    itp = 4

! this is for the combination
    pra=pfor/(pfor+pco2)
    prc=pco2/(pfor+pco2)
!      note that pra + prc = 1.0

    if (iStart > iStop) then
        write(kStdErr,*) 'in cousin1.f need iStart <= iStop'
        write(kStdErr,*) iStart,iStop
