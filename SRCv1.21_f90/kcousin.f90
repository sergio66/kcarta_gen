    endif
    if (iStop > KMaxPtsBox) then
        write(kStdErr,*) 'in cousin1.f need lenf < KMaxPtsBox'
        write(kStdErr,*) iStop,KMaxPtsBox
        Call DoStop
    endif
    if (iStart < 1) then
        write(kStdErr,*) 'in cousin1.f need iStart > 0'
        write(kStdErr,*) iStart
        Call DoStop
    endif

