        chiu = chi(j,itp+1) + (chi(j+1,itp+1) - chi(j,itp+1))*df
        chip = chil + &
        (chiu-chil)*(T-tchi(itp))/(tchi(itp+1)-tchi(itp))
        z(ip)=pra*chif + prc*chip
    enddo

    return
    end subroutine cousin

